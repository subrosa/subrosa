package com.subrosagames.subrosa.domain.game.support.assassin;

/**
 * Enumeration of possible assignment types.
 */
public enum AssignmentType {

    /**
     * Round robin assignments.
     */
    ROUND_ROBIN,
    /**
     * Bi-directional round robin assignments.
     */
    MUTUAL_INTEREST,
    /**
     * Every opponent is an assignment.
     */
    MELEE
}
